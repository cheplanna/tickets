package t1.t1;

// 1. Напишите программу, в которой из строки "I have 3 cats, 4 dogs, and 1 turtle" отбираются
// цифры. Из этих цифр формируется массив.

public class n1 {
    public static void main(String[] args) {
        // Заданная строка, из которой будут отбираться цифры
        String s = "I have 3 cats, 4 dogs, and 1 turtle";

        // Преобразуем строку в массив символов
        char[] chars = s.toCharArray();

        // Массив, в котором будут храниться отобранные цифры
        // Мы создаем массив длиной 10, хотя в заданной строке цифр может быть меньше,
        // чтобы учесть все возможные цифры от 0 до 9
        char[] m = new char[10];

        // Переменная для отслеживания количества найденных цифр
        int count = 0;

        // Проходим по каждому символу в строке
        for (int i = 0; i < s.length(); i++) {
            // Проверяем, является ли текущий символ цифрой
            if (Character.isDigit(chars[i])) {
                // Если символ является цифрой, добавляем его в массив m
                m[count] = chars[i];
                count++;
            }
        }

        // Выводим все элементы массива m
        for (int x = 0; x < m.length; x++){
            System.out.println(m[x]);
        }

    }
}

//    Объяснение кода:
//
//        Объявляется класс n1.
//        В методе main начинается выполнение программы.
//        Задана строка s, содержащая текст с цифрами.
//        Строка s преобразуется в массив символов chars с помощью метода toCharArray().
//        Создается массив m длиной 10 для хранения отобранных цифр. Изначально все элементы массива инициализируются значением по умолчанию, в данном случае, символом с кодом 0.
//        Переменная count инициализируется нулем и будет использоваться для отслеживания количества найденных цифр.
//        Выполняется цикл for, который проходит по каждому символу в строке s.
//        Внутри цикла проверяется, является ли текущий символ цифрой с помощью метода Character.isDigit().
//        Если текущий символ является цифрой, он добавляется в массив m по индексу count, а затем значение count увеличивается на 1.
//        После завершения цикла все найденные цифры сохранены в массиве m.
//        Выполняется еще один цикл for, который выводит каждый элемент массива m на отдельной строке с помощью метода System.out.println().
//        Программа завершается.

